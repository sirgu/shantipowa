        <h5>
            Shanti Powa Orchestra is a big band from northern Italy founded in 2010. They produce a very powerful Crossover of Reggae, Dub, Rap, Ska, Dancehall, Funk & Rock. Because of the different cultural backgrounds of the band members, the lyrics are written in English, Italian, French and German.
On stage Shanti Powa are full of energy; and that combined with their innovative music creates a very unique atmosphere involving the whole crowd. In their career Shanti Powa Orchestra have already played hundreds of shows all over Europe (Rototom Sunsplash (ES), Overjam (SL), Boomtown (UK), Sunrise (DE), Reeds (CH)...) sharing the stage with artists such as Toots & The Maytals, Max Romeo, The Gladiators, Israel Vibrations, Dub Fx, Julian Marley, Mellow Mood, Luciano, Shaggy, Inner Circle & many more.
In 2014 the band released their first album "The Orchestra", followed up by "Peaceful Warriors" in 2016. In 2018 they released their third album "'Til Insanity" All three albums have been produced by Shanti Powa and released through their own independent label "Shanti Powa Records".
In 2020 the band released 4 singles "Formula", "Rolling On, "Heaven" & "Rainy Day" and several remixes with International Producers such as "Aquadrop", "Mikkim", "Narfos", "Danger Marc"& more.
In May 2021 their 4th album "Dreamer" was released, produced by the legendary London based Dub maestro "Gaudi".
This is the time for dreamers and for Shanti Powa this is only the beginning.





